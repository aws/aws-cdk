import { Match, Template } from '../../assertions';
import * as kms from '../../aws-kms';
import { CfnParameter, Stack } from '../../core';
import { OntapFileSystemSecret } from '../lib';

describe('OntapFileSystemSecret', () => {
  let stack: Stack;

  beforeEach(() => {
    stack = new Stack();
  });

  test('creates a Secrets Manager secret with default settings', () => {
    new OntapFileSystemSecret(stack, 'AdminSecret');

    Template.fromStack(stack).hasResourceProperties('AWS::SecretsManager::Secret', {
      Description: { 'Fn::Join': ['', ['Generated by the CDK for stack: ', { Ref: 'AWS::StackName' }]] },
      GenerateSecretString: {
        PasswordLength: 50,
        ExcludeCharacters: '"@/\\',
        GenerateStringKey: 'password',
        SecretStringTemplate: '{"username":"fsxadmin"}',
      },
    });
  });

  test('uses a custom admin username when provided', () => {
    new OntapFileSystemSecret(stack, 'AdminSecret', {
      adminUsername: 'custom-admin',
    });

    Template.fromStack(stack).hasResourceProperties('AWS::SecretsManager::Secret', {
      GenerateSecretString: Match.objectLike({
        SecretStringTemplate: '{"username":"custom-admin"}',
      }),
    });
  });

  test('uses a custom secret name when provided', () => {
    new OntapFileSystemSecret(stack, 'AdminSecret', {
      secretName: 'my-fsx-admin-secret',
    });

    Template.fromStack(stack).hasResourceProperties('AWS::SecretsManager::Secret', {
      Name: 'my-fsx-admin-secret',
    });
  });

  test('uses a custom KMS key when provided', () => {
    const key = new kms.Key(stack, 'Key');

    new OntapFileSystemSecret(stack, 'AdminSecret', {
      encryptionKey: key,
    });

    Template.fromStack(stack).hasResourceProperties('AWS::SecretsManager::Secret', {
      KmsKeyId: stack.resolve(key.keyArn),
    });
  });

  test('merges user-supplied excludeCharacters with the FSx-mandatory exclusions', () => {
    new OntapFileSystemSecret(stack, 'AdminSecret', {
      excludeCharacters: '!#%',
    });

    Template.fromStack(stack).hasResourceProperties('AWS::SecretsManager::Secret', {
      GenerateSecretString: Match.objectLike({
        // FSx-forbidden characters ("@/\) are always preserved alongside the
        // user-supplied list so the generated password is always service-valid.
        ExcludeCharacters: '"@/\\!#%',
      }),
    });
  });

  test('uses a custom passwordLength when provided', () => {
    new OntapFileSystemSecret(stack, 'AdminSecret', {
      passwordLength: 32,
    });

    Template.fromStack(stack).hasResourceProperties('AWS::SecretsManager::Secret', {
      GenerateSecretString: Match.objectLike({
        PasswordLength: 32,
      }),
    });
  });

  test('throws if passwordLength is below the FSx minimum', () => {
    expect(() => new OntapFileSystemSecret(stack, 'AdminSecret', {
      passwordLength: 4,
    })).toThrow(/'passwordLength' must be an integer between 8 and 50/);
  });

  test('throws if passwordLength is above the FSx maximum', () => {
    expect(() => new OntapFileSystemSecret(stack, 'AdminSecret', {
      passwordLength: 64,
    })).toThrow(/'passwordLength' must be an integer between 8 and 50/);
  });

  test('skips passwordLength range check when value is a token', () => {
    const lengthParam = new CfnParameter(stack, 'PwdLen', { type: 'Number', default: 32 });
    expect(() => new OntapFileSystemSecret(stack, 'AdminSecret', {
      passwordLength: lengthParam.valueAsNumber,
    })).not.toThrow();
  });

  test('replicates the secret to the requested regions', () => {
    new OntapFileSystemSecret(stack, 'AdminSecret', {
      replicaRegions: [
        { region: 'us-west-2' },
        { region: 'eu-west-1' },
      ],
    });

    Template.fromStack(stack).hasResourceProperties('AWS::SecretsManager::Secret', {
      ReplicaRegions: [
        { Region: 'us-west-2' },
        { Region: 'eu-west-1' },
      ],
    });
  });

  test('exposes the underlying Secret API (secretArn, secretValueFromJson)', () => {
    const secret = new OntapFileSystemSecret(stack, 'AdminSecret');

    expect(secret.secretArn).toBeDefined();
    expect(secret.secretValueFromJson('password')).toBeDefined();
    expect(secret.secretValueFromJson('username')).toBeDefined();
  });
});
