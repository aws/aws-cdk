import type { Construct } from 'constructs';
import type * as kms from '../../aws-kms';
import * as secretsmanager from '../../aws-secretsmanager';
import { Aws, Token, UnscopedValidationError } from '../../core';
import { addConstructMetadata } from '../../core/lib/metadata-resource';
import { lit } from '../../core/lib/private/literal-string';
import { propertyInjectable } from '../../core/lib/prop-injectable';

/**
 * Characters that FSx for NetApp ONTAP does not accept in admin passwords.
 * These are always excluded from the generated password, even when the caller
 * supplies their own `excludeCharacters` list.
 */
const FSX_FORBIDDEN_PASSWORD_CHARS = '"@/\\';

/**
 * Minimum and maximum password length accepted by FSx for NetApp ONTAP.
 */
const FSX_MIN_PASSWORD_LENGTH = 8;
const FSX_MAX_PASSWORD_LENGTH = 50;

/**
 * Construction properties for an OntapFileSystemSecret.
 */
export interface OntapFileSystemSecretProps {
  /**
   * The admin username.
   *
   * @default 'fsxadmin'
   */
  readonly adminUsername?: string;

  /**
   * A name for the secret.
   *
   * @default - A name is generated by CloudFormation.
   */
  readonly secretName?: string;

  /**
   * The KMS key to use to encrypt the secret.
   *
   * @default - default master key
   */
  readonly encryptionKey?: kms.IKey;

  /**
   * Characters to not include in the generated password.
   *
   * @default '"@/\\'
   */
  readonly excludeCharacters?: string;

  /**
   * The length of the generated password.
   *
   * Must be between 8 and 50 (the FSx for NetApp ONTAP service limits).
   *
   * @default 50
   */
  readonly passwordLength?: number;

  /**
   * A list of regions where to replicate this secret.
   *
   * @default - Secret is not replicated
   */
  readonly replicaRegions?: secretsmanager.ReplicaRegion[];
}

/**
 * A Secrets Manager secret for FSx for NetApp ONTAP admin credentials.
 *
 * Generates a secret with a `username` and `password` field, suitable for use
 * as `fsxAdminPassword` on `OntapFileSystem` or `svmAdminPassword` on
 * `OntapStorageVirtualMachine`.
 *
 * @example
 * declare const vpc: ec2.Vpc;
 * const adminSecret = new fsx.OntapFileSystemSecret(this, 'FsxAdminSecret');
 *
 * new fsx.OntapFileSystem(this, 'Fs', {
 *   vpc,
 *   vpcSubnets: vpc.privateSubnets,
 *   storageCapacityGiB: 1024,
 *   ontapConfiguration: {
 *     deploymentType: fsx.OntapDeploymentType.SINGLE_AZ_1,
 *     throughputCapacityPerHaPair: fsx.ThroughputCapacityPerHaPair.MB_PER_SEC_128,
 *     fsxAdminPassword: adminSecret.secretValueFromJson('password'),
 *   },
 * });
 */
@propertyInjectable
export class OntapFileSystemSecret extends secretsmanager.Secret {
  /** Uniquely identifies this class. */
  public static readonly PROPERTY_INJECTION_ID: string = 'aws-cdk-lib.aws-fsx.OntapFileSystemSecret';

  /**
   * Pre-super range check for `passwordLength` so that an invalid value never
   * reaches the underlying `secretsmanager.Secret` constructor and never leaves
   * an orphaned construct in the tree if the caller catches the error.
   */
  private static validatePasswordLength(passwordLength: number | undefined): void {
    if (passwordLength == null) return;
    // Skip the range check when the value is an unresolved token (e.g. a
    // CfnParameter passed via Token.asNumber()). The deploy-time value is
    // validated by Amazon FSx.
    if (Token.isUnresolved(passwordLength)) return;
    if (!Number.isInteger(passwordLength)
      || passwordLength < FSX_MIN_PASSWORD_LENGTH
      || passwordLength > FSX_MAX_PASSWORD_LENGTH) {
      throw new UnscopedValidationError(
        lit`PasswordLengthOutOfRange`,
        `'passwordLength' must be an integer between ${FSX_MIN_PASSWORD_LENGTH} and ${FSX_MAX_PASSWORD_LENGTH}, got ${passwordLength}`,
      );
    }
  }

  constructor(scope: Construct, id: string, props: OntapFileSystemSecretProps = {}) {
    // Pre-super validation: reject invalid `passwordLength` before any child
    // construct is added to the tree. This mirrors the standard CDK pattern and
    // keeps the construct tree clean if a caller catches the error.
    OntapFileSystemSecret.validatePasswordLength(props.passwordLength);

    // Always include the FSx-forbidden characters in the exclusion list so a
    // caller-supplied `excludeCharacters` cannot accidentally allow them and
    // produce a password the service will reject.
    const userExclude = props.excludeCharacters ?? '';
    const excludeCharacters = Array.from(new Set(FSX_FORBIDDEN_PASSWORD_CHARS + userExclude)).join('');

    const passwordLength = props.passwordLength ?? FSX_MAX_PASSWORD_LENGTH;

    super(scope, id, {
      encryptionKey: props.encryptionKey,
      description: `Generated by the CDK for stack: ${Aws.STACK_NAME}`,
      secretName: props.secretName,
      generateSecretString: {
        passwordLength,
        secretStringTemplate: JSON.stringify({
          username: props.adminUsername ?? 'fsxadmin',
        }),
        generateStringKey: 'password',
        excludeCharacters,
      },
      replicaRegions: props.replicaRegions,
    });

    addConstructMetadata(this, props);
  }
}
