/**
 * L2 construct for `AWS::BedrockAgentCore::WorkloadIdentity`.
 *
 * A workload identity is the stable identity of an agent in an AWS account. It ties together
 * IAM roles, OAuth2 flows, API keys, and workload access tokens for consistent authentication
 * across environments.
 *
 * @see https://docs.aws.amazon.com/AWSCloudFormation/latest/UserGuide/aws-resource-bedrockagentcore-workloadidentity.html
 * @resource AWS::BedrockAgentCore::WorkloadIdentity
 */
@propertyInjectable
export class WorkloadIdentity extends WorkloadIdentityBase {
  /** Uniquely identifies this class. */
  public static readonly PROPERTY_INJECTION_ID: string = 'aws-cdk-lib.aws-bedrockagentcore.WorkloadIdentity';

  /**
   * Import an existing workload identity.
   */
  public static fromWorkloadIdentityAttributes(
    scope: Construct,
    id: string,
    attrs: WorkloadIdentityAttributes,
  ): IWorkloadIdentity {
    class Import extends WorkloadIdentityBase {
      public readonly workloadIdentityArn = attrs.workloadIdentityArn;
      public readonly workloadIdentityName = attrs.workloadIdentityName;
      public readonly createdTime = attrs.createdTime;
      public readonly lastUpdatedTime = attrs.lastUpdatedTime;

      constructor(s: Construct, i: string) {
        super(s, i);
      }
    }
    return new Import(scope, id);
  }

  public readonly workloadIdentityArn: string;
  public readonly workloadIdentityName: string;

  private _createdTime?: string;
  private _lastUpdatedTime?: string;
  private readonly _resource: CfnWorkloadIdentity;

  public get createdTime(): string | undefined {
    if (this._createdTime === undefined) {
      this._createdTime = Token.asString(this._resource.attrCreatedTime);
    }
    return this._createdTime;
  }

  public get lastUpdatedTime(): string | undefined {
    if (this._lastUpdatedTime === undefined) {
      this._lastUpdatedTime = Token.asString(this._resource.attrLastUpdatedTime);
    }
    return this._lastUpdatedTime;
  }

  constructor(scope: Construct, id: string, props: WorkloadIdentityProps = {}) {
    super(scope, id, {
      physicalName: props.workloadIdentityName
        ?? Lazy.string({ produce: () => Names.uniqueResourceName(this, { maxLength: 255 }) }),
    });

    addConstructMetadata(this, props);

    this.workloadIdentityName = this.physicalName;

    throwIfInvalid(validateWorkloadIdentityName, this.workloadIdentityName, this);
    throwIfInvalid(validateAllowedResourceOauth2ReturnUrls, props.allowedResourceOauth2ReturnUrls, this);
    throwIfInvalid(validateCredentialProviderTags, props.tags, this);

    const cfnProps: CfnWorkloadIdentityProps = {
      name: this.workloadIdentityName,
      allowedResourceOauth2ReturnUrls: props.allowedResourceOauth2ReturnUrls,
      tags: props.tags && Object.keys(props.tags).length > 0
        ? Object.entries(props.tags).map(([key, value]) => ({ key, value }))
        : undefined,
    };

    this._resource = new CfnWorkloadIdentity(this, 'Resource', cfnProps);

    this.workloadIdentityArn = this._resource.attrWorkloadIdentityArn;
  }
}
