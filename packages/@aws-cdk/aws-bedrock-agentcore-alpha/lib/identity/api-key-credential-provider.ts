/**
 * An API key credential provider registered in AgentCore Token Vault.
 */
export interface IApiKeyCredentialProvider extends IResource, iam.IGrantable, IApiKeyCredentialProviderRef {
  /**
   * The ARN of this credential provider.
   * @attribute
   */
  readonly credentialProviderArn: string;

  /**
   * The ARN of the Secrets Manager secret that stores the API key after the resource is created.
   *
   * May be undefined for resources imported without this attribute.
   *
   * @attribute
   */
  readonly apiKeySecretArn?: string;

  /**
   * Timestamp when the credential provider was created.
   * @attribute
   */
  readonly createdTime?: string;

  /**
   * Timestamp when the credential provider was last updated.
   * @attribute
   */
  readonly lastUpdatedTime?: string;

  /**
   * Grants IAM actions to the IAM principal.
   */
  grant(grantee: iam.IGrantable, ...actions: string[]): iam.Grant;

  /**
   * Grant `Get` on this provider and `List` for discovery (list is not resource-scoped in IAM).
   */
  grantRead(grantee: iam.IGrantable): iam.Grant;

  /**
   * Grant control plane permissions to manage this provider.
   */
  grantAdmin(grantee: iam.IGrantable): iam.Grant;

  /**
   * Grant permission to retrieve API key material for outbound calls (`GetResourceApiKey`).
   */
  grantUse(grantee: iam.IGrantable): iam.Grant;

  /**
   * Grant read, admin, and credential retrieval permissions.
   */
  grantFullAccess(grantee: iam.IGrantable): iam.Grant;

  /**
   * ARNs for use with gateway targets (`GatewayCredentialProvider.fromApiKeyIdentity` or `fromApiKeyIdentityArn`).
   */
  bindForGatewayApiKeyTarget(): GatewayApiKeyIdentityBinding;
}

/**
 * Provider and secret ARNs for wiring a Token Vault API key identity into a gateway target.
 */
export interface GatewayApiKeyIdentityBinding {
  /**
   * API key credential provider ARN.
   */
  readonly providerArn: string;

  /**
   * Secrets Manager secret ARN for the API key material.
   */
  readonly secretArn: string;
}

/**
 * Properties for a new {@link ApiKeyCredentialProvider} (Token Vault resource).
 */
export interface ApiKeyCredentialProviderResourceProps {
  /**
   * Name of the credential provider.
   *
   * @default a name generated by CDK
   */
  readonly apiKeyCredentialProviderName?: string;

  /**
   * Plaintext API key. If omitted, you can supply the key through another mechanism supported by the service.
   *
   * @default - no key in template (provider may still be created depending on service behavior)
   */
  readonly apiKey?: string;

  /**
   * Tags for this credential provider.
   *
   * @default - no tags
   */
  readonly tags?: { [key: string]: string };
}

/**
 * Attributes for importing an existing API key credential provider.
 */
export interface ApiKeyCredentialProviderAttributes {
  /**
   * ARN of the credential provider.
   */
  readonly credentialProviderArn: string;

  /**
   * ARN of the Secrets Manager secret for the API key, if known.
   *
   * @default - not set; required for {@link ApiKeyCredentialProvider.bindForGatewayApiKeyTarget} on imported providers
   */
  readonly apiKeySecretArn?: string;

  /**
   * Resource creation time.
   *
   * @default - not set
   */
  readonly createdTime?: string;

  /**
   * Resource last-updated time.
   *
   * @default - not set
   */
  readonly lastUpdatedTime?: string;
}

/******************************************************************************
 *                         Abstract base
 *****************************************************************************/

abstract class ApiKeyCredentialProviderBase extends Resource implements IApiKeyCredentialProvider {
  public abstract readonly credentialProviderArn: string;
  public abstract readonly apiKeySecretArn?: string;
  public abstract readonly createdTime?: string;
  public abstract readonly lastUpdatedTime?: string;
  public readonly grantPrincipal: iam.IPrincipal = new iam.UnknownPrincipal({ resource: this });

  public get apiKeyCredentialProviderRef(): ApiKeyCredentialProviderReference {
    return { credentialProviderArn: this.credentialProviderArn };
  }

  constructor(scope: Construct, id: string, props?: ResourceProps) {
    super(scope, id, props);
  }

  /**
   * [disable-awslint:no-grants]
   */
  public grant(grantee: iam.IGrantable, ...actions: string[]): iam.Grant {
    return iam.Grant.addToPrincipal({
      grantee,
      actions,
      resourceArns: [this.credentialProviderArn],
      scope: this,
    });
  }

  /**
   * [disable-awslint:no-grants]
   */
  public grantRead(grantee: iam.IGrantable): iam.Grant {
    return grantReadWithList(
      this,
      grantee,
      this.credentialProviderArn,
      ApiKeyCredentialProviderIdentityPerms.READ_PERMS,
      ApiKeyCredentialProviderIdentityPerms.LIST_PERMS,
    );
  }

  /**
   * [disable-awslint:no-grants]
   */
  public grantAdmin(grantee: iam.IGrantable): iam.Grant {
    return this.grant(grantee, ...ApiKeyCredentialProviderIdentityPerms.ADMIN_PERMS);
  }

  /**
   * [disable-awslint:no-grants]
   */
  public grantUse(grantee: iam.IGrantable): iam.Grant {
    const bedrock = this.grant(grantee, ...ApiKeyCredentialProviderIdentityPerms.USE_PERMS);
    const secret = grantCredentialSecretRead(
      this,
      grantee,
      this.apiKeySecretArn,
      [...TOKEN_VAULT_CREDENTIAL_SECRET_READ_PERMS],
    );
    return secret ? bedrock.combine(secret) : bedrock;
  }

  /**
   * [disable-awslint:no-grants]
   */
  public grantFullAccess(grantee: iam.IGrantable): iam.Grant {
    const bedrock = this.grant(grantee, ...ApiKeyCredentialProviderIdentityPerms.FULL_ACCESS_PERMS);
    const secret = grantCredentialSecretRead(
      this,
      grantee,
      this.apiKeySecretArn,
      [...TOKEN_VAULT_CREDENTIAL_SECRET_READ_PERMS],
    );
    return secret ? bedrock.combine(secret) : bedrock;
  }

  public abstract bindForGatewayApiKeyTarget(): GatewayApiKeyIdentityBinding;
}

