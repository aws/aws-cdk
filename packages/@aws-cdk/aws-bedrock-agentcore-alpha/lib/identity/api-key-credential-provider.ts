/**
 * L2 construct for `AWS::BedrockAgentCore::ApiKeyCredentialProvider`.
 *
 * Use this to register an API key identity in AgentCore Token Vault. To attach the identity to a
 * gateway target, use {@link GatewayCredentialProvider.fromApiKeyIdentity} with this construct, or
 * {@link ApiKeyCredentialProvider.bindForGatewayApiKeyTarget} with {@link GatewayCredentialProvider.fromApiKeyIdentityArn}.
 *
 * @see https://docs.aws.amazon.com/AWSCloudFormation/latest/UserGuide/aws-resource-bedrockagentcore-apikeycredentialprovider.html
 * @resource AWS::BedrockAgentCore::ApiKeyCredentialProvider
 */
@propertyInjectable
export class ApiKeyCredentialProvider extends ApiKeyCredentialProviderBase {
  /** Uniquely identifies this class. */
  public static readonly PROPERTY_INJECTION_ID: string = '@aws-cdk.aws-bedrock-agentcore-alpha.ApiKeyCredentialProvider';

  /**
   * Import an existing API key credential provider.
   */
  public static fromApiKeyCredentialProviderAttributes(
    scope: Construct,
    id: string,
    attrs: ApiKeyCredentialProviderAttributes,
  ): IApiKeyCredentialProvider {
    class Import extends ApiKeyCredentialProviderBase {
      public readonly credentialProviderArn = attrs.credentialProviderArn;
      public readonly apiKeySecretArn = attrs.apiKeySecretArn;
      public readonly createdTime = attrs.createdTime;
      public readonly lastUpdatedTime = attrs.lastUpdatedTime;

      constructor(s: Construct, i: string) {
        super(s, i);
      }

      public bindForGatewayApiKeyTarget(): GatewayApiKeyIdentityBinding {
        if (this.apiKeySecretArn == null || this.apiKeySecretArn === '') {
          throw new ValidationError(
            lit`MissingApiKeySecretArn`,
            'apiKeySecretArn is not available on this ApiKeyCredentialProvider (imported providers must pass apiKeySecretArn in fromApiKeyCredentialProviderAttributes).',
            this,
          );
        }
        return {
          providerArn: this.credentialProviderArn,
          secretArn: this.apiKeySecretArn,
        };
      }
    }
    return new Import(scope, id);
  }

  public readonly credentialProviderArn: string;
  /**
   * The name of this API key credential provider.
   * @attribute
   */
  public readonly apiKeyCredentialProviderName: string;
  public readonly createdTime?: string;
  public readonly lastUpdatedTime?: string;

  private _apiKeySecretArn?: string;
  private readonly __resource: CfnApiKeyCredentialProvider;

  public get apiKeySecretArn(): string | undefined {
    if (!this._apiKeySecretArn) {
      this._apiKeySecretArn = Token.asString(this.__resource.attrApiKeySecretArn);
    }
    return this._apiKeySecretArn;
  }

  constructor(scope: Construct, id: string, props: ApiKeyCredentialProviderResourceProps = {}) {
    super(scope, id, {
      physicalName: props.apiKeyCredentialProviderName
        ?? Lazy.string({
          produce: () => Names.uniqueResourceName(this, { maxLength: 128 }),
        }),
    });

    addConstructMetadata(this, props);

    this.apiKeyCredentialProviderName = this.physicalName;

    throwIfInvalid(validateCredentialProviderName, this.apiKeyCredentialProviderName, this);
    throwIfInvalid(validateApiKeyValue, props.apiKey, this);
    throwIfInvalid(validateCredentialProviderTags, props.tags, this);

    const cfnProps: CfnApiKeyCredentialProviderProps = {
      name: this.apiKeyCredentialProviderName,
      apiKey: props.apiKey,
      tags: props.tags && Object.keys(props.tags).length > 0
        ? Object.entries(props.tags).map(([key, value]) => ({ key, value }))
        : undefined,
    };

    this.__resource = new CfnApiKeyCredentialProvider(this, 'Resource', cfnProps);

    this.credentialProviderArn = this.__resource.attrCredentialProviderArn;
    this.createdTime = this.__resource.attrCreatedTime;
    this.lastUpdatedTime = this.__resource.attrLastUpdatedTime;
  }

  /**
   * ARNs for {@link GatewayCredentialProvider.fromApiKeyIdentity} / {@link GatewayCredentialProvider.fromApiKeyIdentityArn}.
   */
  public bindForGatewayApiKeyTarget(): GatewayApiKeyIdentityBinding {
    if (this.apiKeySecretArn == null || this.apiKeySecretArn === '') {
      throw new ValidationError(
        lit`MissingApiKeySecretArn`,
        'apiKeySecretArn is not available on this ApiKeyCredentialProvider (imported providers must pass apiKeySecretArn in fromApiKeyCredentialProviderAttributes).',
        this,
      );
    }
    return {
      providerArn: this.credentialProviderArn,
      secretArn: this.apiKeySecretArn,
    };
  }
}
