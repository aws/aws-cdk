import { IntegTest } from '@aws-cdk/integ-tests-alpha';
import * as cdk from 'aws-cdk-lib';
import { Policy } from '../../../lib/policy/policy';
import { PolicyEngine } from '../../../lib/policy/policy-engine';
import { PolicyStatement } from '../../../lib/policy/policy-statement';

/**
 * Integration test for Policy and PolicyEngine with auto-generated names.
 *
 * This test validates that PolicyEngine and Policy can be created without
 * specifying explicit names, allowing CDK to auto-generate unique names.
 *
 * Manual verification steps after deployment:
 * 1. Verify PolicyEngine resource is created with auto-generated name
 * 2. Verify Policy resource is created with auto-generated name
 * 3. Check that names follow CDK naming conventions
 */

const app = new cdk.App();
const stack = new cdk.Stack(app, 'PolicyAutoGeneratedNameIntegTestStack');

// Create PolicyEngine with auto-generated name
const policyEngine = new PolicyEngine(stack, 'TestPolicyEngine', {
  description: 'PolicyEngine with auto-generated name',
});

// Create Policy with auto-generated name using builder
new Policy(stack, 'TestPolicy', {
  policyEngine: policyEngine,
  statement: PolicyStatement.permit()
    .forAllPrincipals()
    .onAllActions()
    .onAllResources(),
  description: 'Policy with auto-generated name',
});

// Add output for verification
new cdk.CfnOutput(stack, 'PolicyEngineArn', {
  value: policyEngine.policyEngineArn,
  description: 'ARN of the auto-named policy engine',
});

// Create integration test
new IntegTest(app, 'PolicyAutoGeneratedNameIntegTest', {
  testCases: [stack],
  regions: ['us-east-1'], // Bedrock AgentCore is available in limited regions
  diffAssets: true,
  stackUpdateWorkflow: true,
  cdkCommandOptions: {
    deploy: {
      args: {
        rollback: true,
      },
    },
    destroy: {
      args: {
        force: true,
      },
    },
  },
});

app.synth();
