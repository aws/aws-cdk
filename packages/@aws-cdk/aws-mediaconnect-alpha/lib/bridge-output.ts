import type { IResource } from 'aws-cdk-lib';
import { Resource, Lazy, Names, Token, ValidationError } from 'aws-cdk-lib';
import { CfnBridgeOutput } from 'aws-cdk-lib/aws-mediaconnect';
import type { IBridgeOutputRef, BridgeOutputReference } from 'aws-cdk-lib/aws-mediaconnect';
import { lit } from 'aws-cdk-lib/core/lib/helpers-internal';
import { addConstructMetadata } from 'aws-cdk-lib/core/lib/metadata-resource';
import { propertyInjectable } from 'aws-cdk-lib/core/lib/prop-injectable';
import type { Construct } from 'constructs';
import type { IBridge, BridgeOutputConfiguration } from './bridge';
import { BridgeType } from './bridge';

/**
 * Interface Bridge output
 */
export interface IBridgeOutput extends IResource, IBridgeOutputRef {
  /**
   * The Amazon Resource Name (ARN) of the bridge that owns this output.
   */
  readonly bridgeArn: string;

  /**
   * The name of the bridge output.
   *
   * @attribute
   */
  readonly bridgeOutputName: string;
}

/**
 * Properties for the bridge output
 */
export interface BridgeOutputProps {
  /**
   * Bridge output name.
   *
   * @default - autogenerated
   */
  readonly bridgeOutputName?: string;
  /**
   * The bridge that you want to update.
   */
  readonly bridge: IBridge;

  /**
   * The network output configuration.
   */
  readonly output: BridgeOutputConfiguration;
}

/**
 * Adds outputs to an existing bridge.
 */
@propertyInjectable
export class BridgeOutput extends Resource implements IBridgeOutput {
  /** Uniquely identifies this class. */
  public static readonly PROPERTY_INJECTION_ID: string = '@aws-cdk.aws-mediaconnect-alpha.BridgeOutput';

  public readonly bridgeArn: string;

  /**
   * The name of the bridge output.
   */
  public readonly bridgeOutputName: string;

  /** A reference to this BridgeOutput resource. */
  public get bridgeOutputRef(): BridgeOutputReference {
    return {
      bridgeArn: this.bridgeArn,
      bridgeOutputName: this.bridgeOutputName,
    };
  }

  constructor(scope: Construct, id: string, props: BridgeOutputProps) {
    super(scope, id, {
      physicalName: props.bridgeOutputName ?? Lazy.string({ produce: () => Names.uniqueResourceName(this, { maxLength: 64 }) }),
    });

    // Validate bridge output name if provided
    if (props.bridgeOutputName != null && props.bridgeOutputName !== '' && !Token.isUnresolved(props.bridgeOutputName)) {
      if (props.bridgeOutputName.length < 1 || props.bridgeOutputName.length > 64) {
        throw new ValidationError(lit`BridgeOutputNameLength`, `Bridge output name must be between 1 and 64 characters, got ${props.bridgeOutputName.length}`, this);
      }
      if (!/^[a-zA-Z0-9-]+$/.test(props.bridgeOutputName)) {
        throw new ValidationError(lit`BridgeOutputNameFormat`, `Bridge output name must contain only alphanumeric characters and hyphens, got '${props.bridgeOutputName}'`, this);
      }
    }

    // BridgeOutput can only be attached to an egress bridge. `Bridge.addOutput()`
    // enforces the same check — replicate here so direct construction is safe.
    if (props.bridge.bridgeType.value !== BridgeType.EGRESS_BRIDGE.value) {
      throw new ValidationError(
        lit`BridgeOutputEgressOnly`,
        `BridgeOutput can only be attached to an egress bridge, got '${props.bridge.bridgeType.value}'`,
        this,
      );
    }

    // Enhanced CDK Analytics Telemetry
    addConstructMetadata(this, props);

    const { name: _name, ...networkOutput } = props.output._bind();
    new CfnBridgeOutput(this, 'Resource', {
      name: this.physicalName,
      bridgeArn: props.bridge.bridgeArn,
      networkOutput,
    });

    this.bridgeArn = props.bridge.bridgeArn;
    this.bridgeOutputName = this.physicalName;
  }
}
