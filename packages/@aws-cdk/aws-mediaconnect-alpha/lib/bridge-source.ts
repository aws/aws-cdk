import type { IResource } from 'aws-cdk-lib';
import { Resource, Lazy, Names, Token, ValidationError } from 'aws-cdk-lib';
import { CfnBridgeSource } from 'aws-cdk-lib/aws-mediaconnect';
import type { IBridgeSourceRef, BridgeSourceReference } from 'aws-cdk-lib/aws-mediaconnect';
import { lit } from 'aws-cdk-lib/core/lib/helpers-internal';
import { addConstructMetadata } from 'aws-cdk-lib/core/lib/metadata-resource';
import { propertyInjectable } from 'aws-cdk-lib/core/lib/prop-injectable';
import type { Construct } from 'constructs';
import type { IBridge, BridgeFlowSource } from './bridge';
import { BridgeType } from './bridge';
import type { BridgeNetworkSource } from './shared';

/**
 * Interface Bridge source
 */
export interface IBridgeSource extends IResource, IBridgeSourceRef {
  /**
   * The Amazon Resource Name (ARN) of the bridge that owns this source.
   */
  readonly bridgeArn: string;

  /**
   * The name of the bridge source.
   *
   * @attribute
   */
  readonly bridgeSourceName: string;
}

/**
 * Properties for the bridge source
 */
export interface BridgeSourceProps {
  /**
   * Name of the bridge source.
   *
   * @default - autogenerated
   */
  readonly bridgeSourceName?: string;
  /**
   * The bridge to add this source to.
   */
  readonly bridge: IBridge;

  /**
   * Configuration for Bridge Source Input
   */
  readonly source: BridgeSourceConfiguration;
}

/**
 * Factory class for creating bridge source configurations
 */
export class BridgeSourceConfiguration {
  /**
   * Create a flow source configuration for a bridge source
   * @param source Flow source configuration
   * @returns Bridge source configuration with flow source
   */
  public static flow(source: BridgeFlowSource): BridgeSourceConfiguration {
    return new BridgeSourceConfiguration(source, undefined);
  }

  /**
   * Create a network source configuration for a bridge source
   * @param source Network source configuration
   * @returns Bridge source configuration with network source
   */
  public static network(source: BridgeNetworkSource): BridgeSourceConfiguration {
    return new BridgeSourceConfiguration(undefined, source);
  }

  /**
   * @param flowConfig flow source configuration
   * @param networkConfig network source configuration
   */
  protected constructor(
    private readonly flowConfig?: BridgeFlowSource,
    private readonly networkConfig?: BridgeNetworkSource,
  ) {}

  /**
   * Called when the bridge source configuration is bound to a BridgeSource.
   * @internal
   */
  public _bind() {
    return {
      flowConfig: this.flowConfig,
      networkConfig: this.networkConfig,
    };
  }
}

/**
 * Adds sources to an existing bridge.
 */
@propertyInjectable
export class BridgeSource extends Resource implements IBridgeSource {
  /** Uniquely identifies this class. */
  public static readonly PROPERTY_INJECTION_ID: string = '@aws-cdk.aws-mediaconnect-alpha.BridgeSource';

  public readonly bridgeArn: string;

  /**
   * The name of the bridge source.
   */
  public readonly bridgeSourceName: string;

  /** A reference to this BridgeSource resource. */
  public get bridgeSourceRef(): BridgeSourceReference {
    return {
      bridgeArn: this.bridgeArn,
      bridgeSourceName: this.bridgeSourceName,
    };
  }

  constructor(scope: Construct, id: string, props: BridgeSourceProps) {
    super(scope, id, {
      physicalName: props?.bridgeSourceName ?? Lazy.string({ produce: () => Names.uniqueResourceName(this, { maxLength: 64 }) }),
    });

    // Validate bridge source name if provided
    if (props.bridgeSourceName != null && props.bridgeSourceName !== '' && !Token.isUnresolved(props.bridgeSourceName)) {
      if (props.bridgeSourceName.length < 1 || props.bridgeSourceName.length > 64) {
        throw new ValidationError(lit`BridgeSourceNameLength`, `Bridge source name must be between 1 and 64 characters, got ${props.bridgeSourceName.length}`, this);
      }
      if (!/^[a-zA-Z0-9-]+$/.test(props.bridgeSourceName)) {
        throw new ValidationError(lit`BridgeSourceNameFormat`, `Bridge source name must contain only alphanumeric characters and hyphens, got '${props.bridgeSourceName}'`, this);
      }
    }

    // Enhanced CDK Analytics Telemetry
    addConstructMetadata(this, props);

    // Egress bridges should only use flow sources
    const sourceConfig = props.source._bind();
    if (props.bridge.bridgeType.value === BridgeType.EGRESS_BRIDGE.value && !sourceConfig.flowConfig) {
      throw new ValidationError(lit`BridgeSourceEgressFlowOnly`, 'Egress bridges must use flow sources', this);
    }

    // Ingress bridges should only use network sources
    if (props.bridge.bridgeType.value === BridgeType.INGRESS_BRIDGE.value && !sourceConfig.networkConfig) {
      throw new ValidationError(lit`BridgeSourceIngressNetworkOnly`, 'Ingress bridges must use network sources', this);
    }

    if (!props.bridge.isFailoverEnabled) {
      throw new ValidationError(
        lit`BridgeSourceFailoverRequired`,
        'Failover configuration needs to be configured and enabled to add an additional Bridge source. ' +
        'If the Bridge is imported via fromBridgeAttributes(), set isFailoverEnabled: true',
        this,
      );
    }

    const flowInput = sourceConfig.flowConfig ? {
      flowArn: sourceConfig.flowConfig.flow.flowArn,
      flowVpcInterfaceAttachment: sourceConfig.flowConfig.vpcInterface ? {
        vpcInterfaceName: sourceConfig.flowConfig.vpcInterface.name,
      } : undefined,
    } : undefined;
    const networkInput = sourceConfig.networkConfig ? {
      multicastIp: sourceConfig.networkConfig.multicastIp,
      networkName: sourceConfig.networkConfig.network.name,
      port: sourceConfig.networkConfig.port,
      protocol: sourceConfig.networkConfig.protocol.value,
      multicastSourceSettings: {
        multicastSourceIp: sourceConfig.networkConfig.multicastSourceIp,
      },
    } : undefined;

    new CfnBridgeSource(this, 'Resource', {
      name: this.physicalName,
      bridgeArn: props.bridge.bridgeArn,
      flowSource: flowInput,
      networkSource: networkInput,
    });

    this.bridgeArn = props.bridge.bridgeArn;
    this.bridgeSourceName = this.physicalName;
  }
}
