import type { IResource } from 'aws-cdk-lib';
import { Resource, Lazy, Names, Token, ValidationError } from 'aws-cdk-lib';
import { CfnFlowEntitlement } from 'aws-cdk-lib/aws-mediaconnect';
import type { IFlowEntitlementRef, FlowEntitlementReference } from 'aws-cdk-lib/aws-mediaconnect';
import { lit } from 'aws-cdk-lib/core/lib/helpers-internal';
import { addConstructMetadata } from 'aws-cdk-lib/core/lib/metadata-resource';
import { propertyInjectable } from 'aws-cdk-lib/core/lib/prop-injectable';
import type { Construct } from 'constructs';
import type { IFlow } from './flow';
import type { StaticKeyEncryption } from './shared';
import { renderStaticKeyEncryption } from './shared';

/**
 * Interface for FlowEntitlement
 */
export interface IFlowEntitlement extends IResource, IFlowEntitlementRef {
  /**
   * The Amazon Resource Name (ARN) of the entitlement.
   *
   * @attribute
   */
  readonly entitlementArn: string;
}

/**
 * Options for entitlement status
 */
export enum EntitlementStatus {
  /**
   * Option for enabled
   */
  ENABLED='ENABLED',
  /**
   * Option for disabled
   */
  DISABLED='DISABLED',
}

/**
 * Properties for the Flow entitlement
 */
export interface FlowEntitlementProps {
  /**
   * The name of the entitlement. This value must be unique within the current flow.
   *
   * @default autogenerated
   */
  readonly flowEntitlementName?: string;

  /**
   * The percentage of the entitlement data transfer fee that you want the subscriber to be responsible for.
   *
   * @default 0
   */
  readonly dataTransferSubscriberFeePercent?: number;

  /**
   * A description of the entitlement. This description appears only on the MediaConnect console
   * and is not visible outside of the current AWS account.
   */
  readonly description: string;

  /**
   * Options for encrypting the entitlement.
   * Only static key encryption is supported for entitlements.
   *
   * @default - no encryption
   */
  readonly encryption?: StaticKeyEncryption;

  /**
   * The flow to set the entitlement from.
   */
  readonly flow: IFlow;

  /**
   * The AWS account IDs that you want to share your content with. The receiving accounts (subscribers)
   * will be allowed to create their own flows using your content as the source.
   */
  readonly subscribers: string[];

  /**
   * An indication of whether the new entitlement should be enabled or disabled as soon as it is created.
   * If you don’t specify the entitlementStatus field in your request, MediaConnect sets it to ENABLED.
   *
   * @default - ENABLED
   */
  readonly entitlementStatus?: EntitlementStatus;
}

/**
 * Shared base for both real and imported flow entitlements.
 * @internal
 */
abstract class FlowEntitlementBase extends Resource implements IFlowEntitlement {
  public abstract readonly entitlementArn: string;

  public get flowEntitlementRef(): FlowEntitlementReference {
    return { entitlementArn: this.entitlementArn };
  }
}

/**
 * Resource defines the permission that an AWS account grants to another AWS account to allow access to the content in a specific AWS Elemental MediaConnect flow.
 */
@propertyInjectable
export class FlowEntitlement extends FlowEntitlementBase {
  /** Uniquely identifies this class. */
  public static readonly PROPERTY_INJECTION_ID: string = '@aws-cdk.aws-mediaconnect-alpha.FlowEntitlement';

  /**
   * Creates a FlowEntitlement construct that represents an external (imported) FlowEntitlement.
   */
  public static fromFlowEntitlementArn(scope: Construct, id: string, flowEntitlementArn: string): IFlowEntitlement {
    class Import extends FlowEntitlementBase {
      public readonly entitlementArn = flowEntitlementArn;
    }

    return new Import(scope, id);
  }

  public readonly entitlementArn: string;

  constructor(scope: Construct, id: string, props: FlowEntitlementProps) {
    super(scope, id, {
      physicalName: props?.flowEntitlementName ?? Lazy.string({ produce: () => Names.uniqueResourceName(this, { maxLength: 64 }) }),
    });

    // Validate flow entitlement name if provided
    if (props.flowEntitlementName != null && props.flowEntitlementName !== '' && !Token.isUnresolved(props.flowEntitlementName)) {
      if (props.flowEntitlementName.length < 1 || props.flowEntitlementName.length > 64) {
        throw new ValidationError(lit`FlowEntitlementNameLength`, `Flow entitlement name must be between 1 and 64 characters, got ${props.flowEntitlementName.length}`, this);
      }
      if (!/^[a-zA-Z0-9_-]+$/.test(props.flowEntitlementName)) {
        throw new ValidationError(lit`FlowEntitlementNameFormat`, `Flow entitlement name must contain only alphanumeric characters, hyphens, and underscores, got '${props.flowEntitlementName}'`, this);
      }
    }

    // Validate description length if provided
    if (props.description != null && !Token.isUnresolved(props.description) && props.description.length > 1024) {
      throw new ValidationError(lit`FlowEntitlementDescriptionLength`, `Flow entitlement description must not exceed 1024 characters, got ${props.description.length}`, this);
    }

    // Validate dataTransferSubscriberFeePercent range — service rejects values outside 0-100 at deploy time
    if (props.dataTransferSubscriberFeePercent !== undefined && !Token.isUnresolved(props.dataTransferSubscriberFeePercent)) {
      const pct = props.dataTransferSubscriberFeePercent;
      if (!Number.isInteger(pct) || pct < 0 || pct > 100) {
        throw new ValidationError(
          lit`FlowEntitlementSubscriberFeePercentRange`,
          `dataTransferSubscriberFeePercent must be an integer between 0 and 100, got ${pct}`,
          this,
        );
      }
    }

    // Enhanced CDK Analytics Telemetry
    addConstructMetadata(this, props);

    const resource = new CfnFlowEntitlement(this, 'Resource', {
      name: this.physicalName,
      flowArn: props.flow.flowArn,
      description: props.description,
      subscribers: props.subscribers,
      dataTransferSubscriberFeePercent: props.dataTransferSubscriberFeePercent,
      entitlementStatus: props.entitlementStatus,
      encryption: props.encryption ? renderStaticKeyEncryption(this, props.encryption, props.flow.flowArn) : undefined,
    });

    this.entitlementArn = resource.attrEntitlementArn;
  }
}
