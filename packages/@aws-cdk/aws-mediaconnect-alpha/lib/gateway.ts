/**
 * Interface for Gateway
 */
export interface IGateway extends IResource, IGatewayRef {
  /**
   * The Amazon Resource Name (ARN) of the gateway.
   *
   * @attribute
   */
  readonly gatewayArn: string;

  /**
   * The current state of the gateway.
   *
   * @attribute
   */
  readonly gatewayState?: string;

  /**
   * Create a CloudWatch metric for this gateway.
   *
   * The gateway ARN is set as the `GatewayARN` dimension automatically. For
   * metrics that require additional dimensions (such as `NetworkName`,
   * `InstanceId` for a specific appliance, or `BridgeSourceName`), pass them
   * via `props.dimensionsMap`.
   *
   * See the MediaConnect documentation for available metric names (for example
   * `IngressBridgeBitRate`, `EgressBridgeBitRate`, `IngressBridgeSourcePacketLossPercent`).
   *
   * @see https://docs.aws.amazon.com/mediaconnect/latest/ug/monitor-with-cloudwatch-metrics-gateway-health.html
   */
  metric(metricName: string, props?: MetricOptions): Metric;

  /**
   * Metric for the total number of packets sent from egress bridges hosted on this gateway.
   *
   * Pair with {@link metricEgressBridgeDroppedPackets} to chart an egress dropped-packet
   * percentage. For per-bridge data, pass `dimensionsMap: { BridgeSourceName: '...' }`.
   *
   * @default - sum over 60 seconds
   */
  metricEgressBridgeTotalPackets(props?: MetricOptions): Metric;

  /**
   * Metric for the number of packets dropped by egress bridges hosted on this gateway.
   *
   * Pair with {@link metricEgressBridgeTotalPackets} to chart a dropped-packet percentage.
   * For per-bridge data, pass `dimensionsMap: { BridgeSourceName: '...' }`.
   *
   * @default - sum over 60 seconds
   */
  metricEgressBridgeDroppedPackets(props?: MetricOptions): Metric;

  /**
   * Metric for the total number of packets received by ingress bridges hosted on this gateway.
   *
   * Pair with {@link metricIngressBridgeDroppedPackets} to chart an ingress dropped-packet
   * percentage. For per-bridge data, pass `dimensionsMap: { BridgeSourceName: '...' }`.
   *
   * @default - sum over 60 seconds
   */
  metricIngressBridgeTotalPackets(props?: MetricOptions): Metric;

  /**
   * Metric for the number of packets dropped by ingress bridges hosted on this gateway.
   *
   * Pair with {@link metricIngressBridgeTotalPackets} to chart a dropped-packet percentage.
   * For per-bridge data, pass `dimensionsMap: { BridgeSourceName: '...' }`.
   *
   * @default - sum over 60 seconds
   */
  metricIngressBridgeDroppedPackets(props?: MetricOptions): Metric;
}

/**
 * The network options for a gateway.
 */
export interface GatewayNetwork {
  /**
   * A unique IP address range to use for this network. These IP addresses should be in the form of a Classless Inter-Domain Routing (CIDR) block; for example, 10.0.0.0/16.
   */
  readonly cidrBlock: string;
  /**
   * The name of the network. This name is used to reference the network and must be unique among networks in this gateway.
   */
  readonly name: string;
}

/**
 * Properties for the MediaConnect Gateway.
 */
export interface GatewayProps {
  /**
   * The name of the gateway. This name can not be modified after the gateway is created.
   *
   * @default - autogenerated
   */
  readonly gatewayName?: string;
  /**
   * The range of IP addresses that are allowed to contribute content or initiate output requests for flows communicating with this gateway.
   * These IP addresses should be in the form of a Classless Inter-Domain Routing (CIDR) block; for example, 10.0.0.0/16.
   */
  readonly egressCidrBlocks: string[];

  /**
   * The list of networks in the gateway. A gateway must have between 1 and 4 networks.
   * Provide the initial set here; use `addNetwork()` to add more (up to 4 total).
   */
  readonly networks: GatewayNetwork[];

  /**
   * Policy to apply when the gateway is removed from the stack.
   *
   * @default RemovalPolicy.RETAIN
   */
  readonly removalPolicy?: RemovalPolicy;
}

abstract class GatewayBase extends Resource implements IGateway {
  public abstract readonly gatewayArn: string;
  public abstract readonly gatewayState?: string;

  public get gatewayRef(): GatewayReference {
    return {
      gatewayArn: this.gatewayArn,
    };
  }

  /**
   * Create a CloudWatch metric for this gateway.
   */
  public metric(metricName: string, props?: MetricOptions): Metric {
    return new Metric({
      metricName,
      namespace: 'AWS/MediaConnect',
      ...props,
      dimensionsMap: {
        GatewayARN: this.gatewayArn,
        ...props?.dimensionsMap,
      },
    }).attachTo(this);
  }

  /**
   * Metric for the total number of packets sent from egress bridges hosted on this gateway.
   */
  public metricEgressBridgeTotalPackets(props?: MetricOptions): Metric {
    return this.metric('EgressBridgeTotalPackets', {
      statistic: 'sum',
      period: Duration.seconds(60),
      unit: Unit.COUNT,
      ...props,
    });
  }

  /**
   * Metric for the number of packets dropped by egress bridges hosted on this gateway.
   */
  public metricEgressBridgeDroppedPackets(props?: MetricOptions): Metric {
    return this.metric('EgressBridgeDroppedPackets', {
      statistic: 'sum',
      period: Duration.seconds(60),
      unit: Unit.COUNT,
      ...props,
    });
  }

  /**
   * Metric for the total number of packets received by ingress bridges hosted on this gateway.
   */
  public metricIngressBridgeTotalPackets(props?: MetricOptions): Metric {
    return this.metric('IngressBridgeTotalPackets', {
      statistic: 'sum',
      period: Duration.seconds(60),
      unit: Unit.COUNT,
      ...props,
    });
  }

  /**
   * Metric for the number of packets dropped by ingress bridges hosted on this gateway.
   */
  public metricIngressBridgeDroppedPackets(props?: MetricOptions): Metric {
    return this.metric('IngressBridgeDroppedPackets', {
      statistic: 'sum',
      period: Duration.seconds(60),
      unit: Unit.COUNT,
      ...props,
    });
  }
}

/**
 * AWS Elemental MediaConnect Gateway is a feature of MediaConnect that allows the deployment of on-premises resources for transporting live
 * video to and from the AWS Cloud. MediaConnect Gateway allows you to contribute live video to the AWS Cloud from on-premises hardware,
 * as well as distribute live video from the AWS Cloud to your local data center.
 */
@propertyInjectable
export class Gateway extends GatewayBase implements IGateway {
  /** Uniquely identifies this class. */
  public static readonly PROPERTY_INJECTION_ID: string = '@aws-cdk.aws-mediaconnect-alpha.Gateway';

  /**
   * Creates a MediaConnect Gateway construct that represents an external (imported) MediaConnect Gateway.
   */
  public static fromGatewayArn(scope: Construct, id: string, gatewayArn: string): IGateway {
    class Import extends GatewayBase implements IGateway {
      public readonly gatewayArn = gatewayArn;
      public readonly gatewayState = undefined;
    }

    return new Import(scope, id);
  }

  public readonly gatewayArn: string;
  public readonly gatewayState?: string;
  private networks: GatewayNetwork[] = [];

  constructor(scope: Construct, id: string, props: GatewayProps) {
    super(scope, id, {
      physicalName: props?.gatewayName ?? Lazy.string({ produce: () => Names.uniqueResourceName(this, { maxLength: 32 }) }),
    });

    // Validate gateway name if provided
    if (props.gatewayName != null && props.gatewayName !== '' && !Token.isUnresolved(props.gatewayName)) {
      if (props.gatewayName.length < 1 || props.gatewayName.length > 32) {
        throw new ValidationError(lit`GatewayNameLength`, `Gateway name must be between 1 and 32 characters, got ${props.gatewayName.length}`, this);
      }
      if (!/^[a-zA-Z0-9-]+$/.test(props.gatewayName)) {
        throw new ValidationError(lit`GatewayNameFormat`, `Gateway name must contain only alphanumeric characters and hyphens, got '${props.gatewayName}'`, this);
      }
    }

    // Enhanced CDK Analytics Telemetry
    addConstructMetadata(this, props);

    // Warn on fully-open egress CIDR blocks (0.0.0.0/0 or any /0 prefix).
    if (!Token.isUnresolved(props.egressCidrBlocks)) {
      for (const cidr of props.egressCidrBlocks) {
        if (!Token.isUnresolved(cidr) && isOpenCidr(cidr)) {
          Annotations.of(this).addWarningV2(
            '@aws-cdk/aws-mediaconnect-alpha:openEgressCidr',
            `Gateway egress CIDR '${cidr}' allows traffic from any IP. Restrict to the narrowest range your flows need.`,
          );
        }
      }
    }

    props.networks.forEach(network => {
      this.networks.push(network);
    });

    const resource = new CfnGateway(this, 'Resource', {
      name: this.physicalName,
      egressCidrBlocks: props.egressCidrBlocks,
      networks: Lazy.any({
        produce: () => {
          if (this.networks.length === 0) throw new ValidationError(lit`GatewayMinNetworks`, 'At least 1 network must be specified on the gateway', this);
          if (this.networks.length > 4) throw new ValidationError(lit`GatewayMaxNetworks`, `A gateway supports a maximum of 4 networks, got ${this.networks.length}`, this);
          return this.networks;
        },
      }, { omitEmptyArray: true }),
    });

    this.gatewayArn = resource.attrGatewayArn;
    this.gatewayState = resource.attrGatewayState;

    resource.applyRemovalPolicy(props.removalPolicy);
  }

  /**
   * Add a network to this gateway.
   */
  public addNetwork(network: GatewayNetwork): GatewayNetwork {
    this.networks.push(network);
    return network;
  }
}
