import { App, Stack } from 'aws-cdk-lib';
import { Annotations, Match, Template } from 'aws-cdk-lib/assertions';
import { Vpc, SecurityGroup } from 'aws-cdk-lib/aws-ec2';
import { RouterNetworkConfiguration, RouterNetworkInterface } from '../lib/router-network-interface';

let app: App;
let stack: Stack;
beforeEach(() => {
  app = new App();
  stack = new Stack(app, undefined, {
    env: { account: '123456789012', region: 'us-east-1' },
  });
});

test('MediaConnect router network interface with public configuration', () => {
  new RouterNetworkInterface(stack, 'network', {
    routerNetworkInterfaceName: 'test-network',
    configuration: RouterNetworkConfiguration.publicNetwork({
      cidr: ['10.0.0.0/16', '192.168.1.0/24'],
    }),
    regionName: 'us-west-2',
    tags: {
      Environment: 'test',
      Project: 'media-streaming',
    },
  });

  Template.fromStack(stack).hasResourceProperties('AWS::MediaConnect::RouterNetworkInterface', {
    Name: 'test-network',
    Configuration: {
      Public: {
        AllowRules: [
          {
            Cidr: '10.0.0.0/16',
          },
          {
            Cidr: '192.168.1.0/24',
          },
        ],
      },
    },
    RegionName: 'us-west-2',
    Tags: [
      {
        Key: 'Environment',
        Value: 'test',
      },
      {
        Key: 'Project',
        Value: 'media-streaming',
      },
    ],
  });
});

test('MediaConnect router network interface with VPC configuration', () => {
  const vpc = new Vpc(stack, 'TestVpc', {
    maxAzs: 2,
  });

  const securityGroup = new SecurityGroup(stack, 'TestSG', {
    vpc,
    description: 'Test security group',
  });

  new RouterNetworkInterface(stack, 'network', {
    configuration: RouterNetworkConfiguration.vpc({
      securityGroups: [securityGroup],
      subnet: vpc.privateSubnets[0],
    }),
  });

  Template.fromStack(stack).hasResourceProperties('AWS::MediaConnect::RouterNetworkInterface', {
    Configuration: {
      Vpc: {
        SecurityGroupIds: [
          {
            'Fn::GetAtt': ['TestSG581D3391', 'GroupId'],
          },
        ],
        SubnetId: {
          Ref: 'TestVpcPrivateSubnet1SubnetCC65D771',
        },
      },
    },
  });
});

test('MediaConnect router network interface with autogenerated name', () => {
  new RouterNetworkInterface(stack, 'network', {
    configuration: RouterNetworkConfiguration.publicNetwork({
      cidr: ['10.1.0.0/16'],
    }),
  });

  Template.fromStack(stack).hasResourceProperties('AWS::MediaConnect::RouterNetworkInterface', {
    Configuration: {
      Public: {
        AllowRules: [
          {
            Cidr: '10.1.0.0/16',
          },
        ],
      },
    },
  });
});

test('imported router network interface throws when accessing routerNetworkInterfaceId', () => {
  const imported = RouterNetworkInterface.fromRouterNetworkInterfaceArn(stack, 'ImportedNI', 'arn:aws:mediaconnect:us-east-1:123456789012:router-network-interface:test');
  expect(() => imported.routerNetworkInterfaceId).toThrow(/routerNetworkInterfaceId.*is not available/);
});

test('imported router network interface has undefined createdAt and updatedAt', () => {
  const imported = RouterNetworkInterface.fromRouterNetworkInterfaceArn(stack, 'ImportedNI2', 'arn:aws:mediaconnect:us-east-1:123456789012:router-network-interface:test');
  expect(imported.createdAt).toBeUndefined();
  expect(imported.updatedAt).toBeUndefined();
});

describe('open public CIDR warning', () => {
  test.each(['0.0.0.0/0', '::/0'])('warns when a public CIDR is fully open (%s)', (openCidr) => {
    new RouterNetworkInterface(stack, 'network', {
      routerNetworkInterfaceName: 'test-network',
      configuration: RouterNetworkConfiguration.publicNetwork({ cidr: [openCidr] }),
      regionName: 'us-west-2',
    });

    Annotations.fromStack(stack).hasWarning(
      '*',
      Match.stringLikeRegexp(`Router network interface public CIDR '${openCidr.replace('/', '\\/')}' allows traffic from any IP`),
    );
  });

  test('does not warn for a narrow public CIDR', () => {
    new RouterNetworkInterface(stack, 'network', {
      routerNetworkInterfaceName: 'test-network',
      configuration: RouterNetworkConfiguration.publicNetwork({ cidr: ['10.0.0.0/16'] }),
      regionName: 'us-west-2',
    });

    Annotations.fromStack(stack).hasNoWarning('*', Match.stringLikeRegexp('allows traffic from any IP'));
  });
});
