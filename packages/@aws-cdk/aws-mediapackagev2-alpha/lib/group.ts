/**
 * Defines an AWS Elemental MediaPackage V2 Channel Group
 */
@propertyInjectable
export class ChannelGroup extends ChannelGroupBase implements IChannelGroup {
  /** Uniquely identifies this class. */
  public static readonly PROPERTY_INJECTION_ID: string = '@aws-cdk.aws-mediapackagev2-alpha.ChannelGroup';

  /**
   * The date and time the channel group was created.
   */
  public readonly createdAt: string;

  /**
   * The output domain where the source stream should be sent. Integrate the egress domain with a downstream CDN (such as Amazon CloudFront) or playback device.
   */
  public readonly egressDomain: string;

  /**
   * The name that describes the channel group. The name is the primary identifier for the channel group, and must be unique for your account in the AWS Region.
   */
  public readonly channelGroupName: string;

  /**
   * The Amazon Resource Name (ARN) associated with the resource.
   */
  public readonly channelGroupArn: string;

  constructor(scope: Construct, id: string, props?: ChannelGroupProps) {
    super(scope, id, {
      physicalName: props?.channelGroupName ?? Lazy.string({
        produce: () => Names.uniqueResourceName(this, {
          maxLength: 256,
        }),
      }),
    });
    // Enhanced CDK Analytics Telemetry
    addConstructMetadata(this, props);

    // Validate channelGroupName if provided
    if (props?.channelGroupName != null) {
      if (props.channelGroupName.length < 1 || props.channelGroupName.length > 256) {
        throw new ValidationError('ChannelGroupNameLength', 'Channel group name must be between 1 and 256 characters in length.', this);
      }
      if (!props.channelGroupName.match(/^[a-zA-Z0-9_-]+$/)) {
        throw new ValidationError('ChannelGroupNamePattern', 'Channel group name must only contain alphanumeric characters, hyphens, and underscores.', this);
      }
    }

    // Validate description if provided
    if (props?.description && props.description.length > 1024) {
      throw new ValidationError('ChannelGroupDescriptionLength', 'Channel group description must not exceed 1024 characters.', this);
    }

    const channelGroup = new CfnChannelGroup(this, 'Resource', {
      channelGroupName: this.physicalName,
      description: props?.description,
      tags: props?.tags ? renderTags(props.tags) : undefined,
    });
    this.createdAt = channelGroup.attrCreatedAt;
    this.egressDomain = channelGroup.attrEgressDomain;
    this.channelGroupName = channelGroup.channelGroupName;
    this.channelGroupArn = channelGroup.attrArn;

    channelGroup.applyRemovalPolicy(props?.removalPolicy ?? RemovalPolicy.DESTROY);
  }
}
